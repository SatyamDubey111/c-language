// Write a program to check whether a given number is an even or an odd number.

#include <stdio.h>
int main()
{
   int a;
   printf("Enter a number");
   scanf("%d",&a);
   if(a%2==0)
   {

    printf("Number is even  ");

   }

   else
   
   {
    printf("Number is odd");
   }

   printf("/n");
   return 0;



}